CREATE PROCEDURE dbo.TPW_DBMS_ALERT_EXPIRE
AS
	SET NOCOUNT ON;

	DELETE	FROM	TPW_DBMS_ALERT
	WHERE	EXPIRY_TIME	< GETDATE();

